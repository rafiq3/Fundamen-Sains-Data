### Binomial Random Variable
###
### For example, we tossing a coin 10 times, let X be the number
### of the outcome “heads” (our “success”), and the probability
### of success is 1/2 in each of the 10 trials. What is the probability
### that we get X = 6 success out of 10 trials?

dbinom(x = 6, size = 10, prob = 1/2)